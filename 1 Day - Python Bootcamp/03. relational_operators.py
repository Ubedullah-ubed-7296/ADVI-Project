a = int(input("Enter a value: "))
b = int(input("Enter b value: "))

c = a > b
print("a > b = ", c)

c = a < b
print("a < b = ", c)

c = a >= b
print("a >= b = ", c)

c = a <= b
print("a <= b = ", c)

c = a == b
print("a == b = ", c)

c = a != b
print("a != b = ", c)

# Output:
# Enter a value: 10
# Enter b value: 20
# a > b =  False
# a < b =  True
# a >= b =  False
# a <= b =  True
# a == b =  False
# a != b =  True